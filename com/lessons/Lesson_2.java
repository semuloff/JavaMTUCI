package com.lessons;

public class Lesson_2 {
    public static void main(String[] args) {
        // variables and data types.
        // parallel study of C ++ bears fruit: the principle of languages is very similar. :3
        int age = 7;
        System.out.println("Your age: " + age);

        // data type "byte" can store a number -128>=number<=127. For so small numbers.
        byte small_number = 127;

        // data type "short" can store a number -32768>=number<=32767. For small numbers.
        short short_number = 32403;

        // data type "int" can store a number -2147483648>=number<=2147483647. For big numbers.
        int int_number = 21312123;

        // data type "long" can store a number -9223372036854775808>=number<=9223372036854775807. For so big numbers.
        long long_number = 223368;

        // it is considered good form to assign a literal "f" to the value for the float type.
        float float_number = 3.3242f;

        // the "double" data type doubles the accuracy
        // of the non-integer part of the number compared to the float data type.
        double double_float_number = 3.23123123f;

        // the "char" data type stores a single character. Variable value is specified in single quotes!
        char symbol = 's';

        // The "String" data type stores a set of characters, that is, a string.
        // The value of a variable for this type of data is specified in double quotes!
        String username = "Serezha228";

        // "boolean" data type can store boolean values: true or false.
        boolean flag = true;
    }
}
